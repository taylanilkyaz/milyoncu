    <div class="ui text container">
        <h2 class="ui header">MADDE 5 - Teslimat Masrafları Ve İfası</h2>
        <p>Mal/Hizmet’in teslimat masrafları Alıcı'ya aittir. Satıcı’nın, websitesinde teslimat
            ücretinin kendisince karşılanacağını beyan etmişse, teslimat masrafları Satıcı’ya ait
            olacaktır. Malın teslimatı; Satıcı’nın stokunun müsait olması ve ödemenin
            gerçekleşmesinden sonra taahhüt edilen sürede yapılır. Satıcı Mal/Hizmet’i, Alıcı tarafından Mal/Hizmet’in sipariş
            edilmesinden itibaren 30 (otuz) gün içinde teslim eder ve bu süre içinde yazılı bildirimle ek 10 (on) günlük süre uzatım
            hakkını saklı tutar. Herhangi bir nedenle Aıcı tarafından Mal/Hizmet bedeli ödenmez veya yapılan ödeme banka kayıtlarında
            iptal edilir ise, Satıcı Mal/Hizmet’in teslimi yükümlülüğünden kurtulmuş kabul edilir.</p>
