        <h2 class="ui header">MADDE 8 – Cayma Hakkı</h2>
        <p>Alıcı’nın hiçbir hukuki ve cezai sorumluluk üstlenmeksizin ve hiçbir gerekçe göstermeksizin
            teslim aldığı veya sözleşmenin imzalandığı tarihten itibaren 7 (yedi) gün içerisinde malı veya
            hizmeti reddederek sözleşmeden cayma hakkının var olduğunu ve cayma bildiriminin Satıcı’ya ulaşması
            tarihinden itibaren Satıcı’nın malı geri alacağını Satıcı taahhüt eder. Cayma hakkı bildirimi ve sözleşmeye
            ilişkin sair bildirimler satıcıya ait ve yukarıda belirtilen satıcı iletişim bilgileri kanalı ile gönderilebilecektir.
            DP, İŞBU MESAFELİ SATIŞ SÖZLEŞMESİNİN TARAFI OLMADIĞINDAN DP’YE KARŞI CAYMA HAKKI KULLANILAMAZ VEYA BEDEL İADESİ TALEP EDİLEMEZ.
        </p>

        <p>
            Cayma hakkının kullanılması için bu süre içerisinde Satıcı’ya
            mevzuat hükümlerine uygun olarak bildirimde bulunulması şarttır.
            Cayma hakkının kullanılması halinde: a) Alıcı’ya veya onun bildirdiği
            yukarıda bilgisi bulunan üçüncü kişiye teslim edilen ürünün iade edilmesi zorunludur.
            b) 7 (yedi) günlük süre içerisinde iade edilecek ürünlerin kutusu, ambalajı, varsa standart
            aksesuarları varsa ürünle birlikte hediye edilen diğer ürünleri eksiksiz ve hasarsız olarak teslim edilmesi
            gerekmektedir. Cayma hakkının kullanılmasını ve bilginin ulaşımını takip eden 10 (on) gün
            içerisinde ürün bedeli Alıcı’ya ödediği şekilde iade edilir. Ürün Satıcı’ya iade edilirken,
            ürün teslimi sırasında Alıcı’ya ibraz edilmiş olan orijinal faturanın da iade edilmesi gerekmektedir.
            İade kargo bedeli Satıcı’ya aittir. Ürünle beraber iade edilecek olan faturanın iade bölümü doldurularak
            ve Alıcı tarafından imzalanarak iade edilecektir.
        </p>


