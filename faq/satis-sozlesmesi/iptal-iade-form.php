    <div class="ui text container">
        <h2 class="ui header">MADDE 6 - Alıcının Beyan Ve Taahhütleri</h2>
        <p>
            Alıcı, Websitesinde yer alan sözleşme konusu ürünün temel nitelikleri, satış fiyatı
            ve ödeme şekli ile teslimata ilişkin olarak Satıcı tarafından yüklenen ön bilgileri
            okuyup bilgi sahibi olduğunu ve elektronik ortamda gerekli teyidi verdiğini beyan eder.
            Alıcılar Tüketici sıfatıyla talep ve şikayetlerini yukarıda yer alan Satıcı iletişim bilgilerinin
            sağladığı kanallarla ulaştırabilirler.
        </p>
        <p>
            Alıcı, işbu Sözleşme’yi ve Ön Bilgilendirme Formunu elektronik ortamda teyit etmekle,
            mesafeli sözleşmelerin akdinden önce Satıcı tarafından tüketiciye verilmesi gereken adres,
            siparişi verilen ürünlere ait temel özellikler, ürünlerin vergiler dahil fiyatı, ödeme ve teslimat
            bilgilerini de doğru ve eksiksiz olarak edindiğini teyit etmiş olur.
        </p>

        <p>
            Alıcı’nın, Sözleşme konusu Mal/Hizmet’i teslim almadan önce
            muayene edecek tahrip olmuş, kırık, ambalajı yırtılmış vb. hasarlı
            ve ayıplı Mal/Hizmeti kargo şirketinden teslim alması halinde sorumluluk
            tamamen kendisine aittir. Alıcı tarafından kargo şirketi görevlisinden
            teslim alınan Mal/Hizmet’in hasarsız ve sağlam olduğu kabul edilecektir.
            Teslimden sonra Mal/Hizmet’in sorumluluğu ve hasarlar Alıcı'ya aittir.
            Mal/Hizmet’in tesliminden sonra Alıcı'ya ait kredi kartının Alıcı'nın kusurundan
            kaynaklanmayan bir şekilde yetkisiz kişilerce haksız veya hukuka aykırı olarak kullanılması
            nedeni ile ilgili banka veya finans kuruluşunun mal/hizmet bedelini Satıcı'ya ödememesi halinde,
            Alıcı kendisine teslim edilmiş olması kaydıyla Mal/Hizmet’i 3 (üç) gün içinde Satıcı'ya iade etmekle
            yükümlüdür. Bu halde teslimat giderleri Alıcı'ya aittir.
        </p>


